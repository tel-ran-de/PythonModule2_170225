# Функция range() используется для создания последовательности чисел.
# Она часто применяется в циклах for для повторения блока кода заданное количество раз.

# Пример 1: range(стоп)
# Создает последовательность чисел от 0 до стоп-1.
# В данном случае, от 0 до 4.
for i in range(5):
    # Цикл выполнится 5 раз, i будет принимать значения 0, 1, 2, 3, 4.
    print(f"Итерация {i}")

# Пример 2: range(старт, стоп)
# Создает последовательность чисел от старт до стоп-1.
# В данном случае, от 2 до 6.
for j in range(2, 7):
    # Цикл выполнится 5 раз, j будет принимать значения 2, 3, 4, 5, 6.
    print(f"Значение j: {j}")

# Пример 3: range(старт, стоп, шаг)
# Создает последовательность чисел от старт до стоп-1 с заданным шагом.
# В данном случае, от 1 до 9 с шагом 2.
for k in range(1, 10, 2):
    # Цикл выполнится 5 раз, k будет принимать значения 1, 3, 5, 7, 9.
    print(f"Нечетное значение k: {k}")

# Пример 4: Использование range() для итерации по индексам списка.
my_list = ['яблоко', 'банан', 'вишня']
for index in range(len(my_list)):
    # len(my_list) возвращает длину списка (3).
    # range(len(my_list)) создает последовательность 0, 1, 2.
    # index будет принимать значения 0, 1, 2, которые являются индексами элементов списка.
    print(f"Элемент с индексом {index}: {my_list[index]}")

# Пример 5: Использование range() для обратного цикла.
for x in range(5, 0, -1):
    # Цикл выполнится 5 раз, x будет принимать значения 5, 4, 3, 2, 1.
    print(f"Обратный отсчет: {x}")